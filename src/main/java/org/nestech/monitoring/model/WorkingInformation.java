package org.nestech.monitoring.model;

public class WorkingInformation {
    public String day_name;
    public String date;
    public int is_day_off;
    public String start_from;
    public int working_hours;

    public String getDay_name() {
        return day_name;
    }

    public String getDate() {
        return date;
    }

    public int getIs_day_off() {
        return is_day_off;
    }

    public String getStart_from() {
        return start_from;
    }

    public int getWorking_hours() {
        return working_hours;
    }
}
